'use strict'

const events = require('./auth/events')
$(() => {
  $('#sign-up-form').on('submit', events.onSignUp)
  $('#sign-in-form').on('submit', events.onSignIn)
  $('#change-password-form').on('submit', events.onChangePassword)
  $('#sign-out').on('submit', events.onSignOut)
  $('#change-password-form').hide()
  $('#sign-out').hide()
  $('#start-game').on('click', events.onStartGame)
  $('#check-games').on('click', events.onCheckGame)
  $('#start-game').hide()
  $('#check-games').hide()
  $('#game-board').hide()
  $('#top-left').on('click', events.onTopLeft)
  $('#top-middle').on('click', events.onTopMiddle)
  $('#top-right').on('click', events.onTopRight)
  $('#middle-left').on('click', events.onMiddleLeft)
  $('#middle-middle').on('click', events.onMiddleMiddle)
  $('#middle-right').on('click', events.onMiddleRight)
  $('#bottom-left').on('click', events.onBottomLeft)
  $('#bottom-middle').on('click', events.onBottomMiddle)
  $('#bottom-right').on('click', events.onBottomRight)
})
